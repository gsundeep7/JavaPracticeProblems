package test.example.Javapractice.DataypesandVariables;

//Write a program to declare all primitive data types and print their values.

public class Primitivedatatypes {

    public static void main(String[] args)
    {
        int i = 24;
        String s = "Sundeep";
        char c = 'b';
        float f = 0.24f;
        double d = 0.1314;
        boolean b = true;


        System.out.println("The integer declared is "+ i);
        System.out.println("The string declared is "+ s);
        System.out.println(c +  " is the char declared is ");
        System.out.println("The float value declared is "+ f);
        System.out.println("The double value declared is "+ d);
        System.out.println("The boolean declared is "+ b);
    }
}
